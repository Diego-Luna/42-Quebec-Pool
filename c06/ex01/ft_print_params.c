#include <unistd.h>

int	main(int arg, char **str)
{
	int	i;
	int	n;

	n = 1;
	i = 0;
	if (arg > 1)
	{
		while (str[n])
		{
			while (str[n][i])
			{
				write(1, &str[n][i], 1);
				i++;
			}
			n++;
			i = 0;
			write(1, "\n", 1);
		}
	}
}
