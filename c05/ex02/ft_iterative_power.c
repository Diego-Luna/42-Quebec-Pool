int	ft_iterative_power(int nb, int power)
{
	int	result;
	int	i;

	i = 0;
	result = nb;
	if (power < 0)
		return (0);
	if (nb == 0 && power == 0)
		return (1);
	while (i < power)
	{
		result *= nb;
		i ++;
	}
	return (result);
}
